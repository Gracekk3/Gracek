day = int(input("Zadejte číslo dne v týdnu (1-7): "))

if day == 1:
    print("Pondělí")
elif day == 2:
    print("Úterý")
elif day == 3:
    print("Středa")
elif day == 4:
    print("Čtvrtek")
elif day == 5:
    print("Pátek")
elif day == 6:
    print("Sobota")
elif day == 7:
    print("Neděle")
else:
    print("Neplatné číslo dne.")
